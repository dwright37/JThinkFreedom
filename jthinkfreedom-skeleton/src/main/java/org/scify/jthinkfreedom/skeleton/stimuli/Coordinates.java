package org.scify.jthinkfreedom.skeleton.stimuli;

/**
 *
 * @author peustr
 */
public interface Coordinates {

    /**
     * Returns the X coordinate associated with a stimulus.
     *
     * @return The coordinate on the X axis.
     */
    public int getX();

    /**
     * Returns the Y coordinate associated with a stimulus.
     *
     * @return The coordinate on the Y axis.
     */
    public int getY();

}
